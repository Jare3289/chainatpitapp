require_once __DIR__ . '/config.php';

$title = '🆕 อัปเดตระบบ CNP APP — 27-28 พฤษภาคม 2569';

// ตรวจว่ามีประกาศนี้แล้วหรือยัง
$check = $pdo->prepare("SELECT id FROM public_relations WHERE title = ? LIMIT 1");
$check->execute([$title]);
if ($check->fetch()) {
    echo "⚠️ ประกาศนี้มีอยู่แล้วในระบบ — ไม่ได้เพิ่มซ้ำ";
    exit;
}

