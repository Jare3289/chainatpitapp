$admin = $pdo->query("SELECT id, CONCAT(COALESCE(prefix,''), first_name, ' ', last_name) AS full_name
                       FROM users WHERE role = 'admin' ORDER BY id ASC LIMIT 1")->fetch(PDO::FETCH_ASSOC);
if (!$admin) {
    $admin = $pdo->query("SELECT id, username AS full_name FROM users WHERE role = 'admin' LIMIT 1")->fetch(PDO::FETCH_ASSOC);
}
$authorId   = $admin['id'] ?? 1;
$authorName = trim($admin['full_name'] ?? 'ผู้ดูแลระบบ') ?: 'ผู้ดูแลระบบ';

