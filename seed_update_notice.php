$stmt = $pdo->prepare("INSERT INTO public_relations
    (title, content, category, visibility, author_id, author_role, author_name, status, approved_at, approved_by)
    VALUES (?, ?, 'ประกาศสำคัญ', 'all', ?, 'admin', ?, 'approved', NOW(), ?)");
$stmt->execute([$title, $content, $authorId, $authorName, $authorId]);

